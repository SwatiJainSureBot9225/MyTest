package com.example.swatijain.mytest.Responce;

public class Vendor {

    private String vendor_ID, vendor_name, vendor_email, vendor_phone, vendor_address, vendor_created, vendor_modified;
    private String vendor_name_hint = "---Select Vendor Name---";

    public String getVendor_ID() {
        return vendor_ID;
    }

    public void setVendor_ID(String vendor_ID) {
        this.vendor_ID = vendor_ID;
    }

    public String getVendor_name() {
        return vendor_name;
    }

    public void setVendor_name(String vendor_name) {
        this.vendor_name = vendor_name;
    }

    public String getVendor_email() {
        return vendor_email;
    }

    public void setVendor_email(String vendor_email) {
        this.vendor_email = vendor_email;
    }

    public String getVendor_phone() {
        return vendor_phone;
    }

    public void setVendor_phone(String vendor_phone) {
        this.vendor_phone = vendor_phone;
    }

    public String getVendor_address() {
        return vendor_address;
    }

    public void setVendor_address(String vendor_address) {
        this.vendor_address = vendor_address;
    }

    public String getVendor_created() {
        return vendor_created;
    }

    public void setVendor_created(String vendor_created) {
        this.vendor_created = vendor_created;
    }

    public String getVendor_modified() {
        return vendor_modified;
    }

    public void setVendor_modified(String vendor_modified) {
        this.vendor_modified = vendor_modified;
    }

    public String getVendor_name_hint() {
        return vendor_name_hint;
    }

    public void setVendor_name_hint(String vendor_name_hint) {
        this.vendor_name_hint = vendor_name_hint;
    }
}
